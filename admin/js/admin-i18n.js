function adminT(key) {
    var lang = (window.ADMIN_EDIT_LANG || localStorage.getItem('gf-admin-lang') || 'en');
    var pack = ADMIN_I18N[lang] || ADMIN_I18N.en;
    var parts = key.split('.');
    var cur = pack;
    for (var i = 0; i < parts.length; i++) {
        if (cur == null) return key;
        cur = cur[parts[i]];
    }
    return cur == null ? key : cur;
}
